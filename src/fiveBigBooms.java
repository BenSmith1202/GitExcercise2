public class fiveBigBooms {
    public static void main(String[] args) {
        for (int i = 0; i < 5; i++) {
            System.out.print("BOO");
            for (int j = 0; j < (Math.pow(3, i)); j++) {
                System.out.print("OO");
            }
            System.out.println("M!");
        }
    }
}
